    /** Initialize with saved or browser language */
    init() {
        const saved = localStorage.getItem('okc-lang');
        const browserLang = (navigator.language || '').slice(0, 2);
        this._lang = saved || (this.translations[browserLang] ? browserLang : 'de');
        this.apply();
        return this._lang;
    },

    setLang(lang) {
        if (!this.translations[lang]) lang = 'de';
        this._lang = lang;
        localStorage.setItem('okc-lang', lang);
        this.apply();
    },

    getLang() { return this._lang; },

    t(key) {
        const dict = this.translations[this._lang] || this.translations.de;
        return dict[key] || this.translations.de[key] || key;
    },

    apply() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = this.t(key);
            if (text !== key) el.textContent = text;
        });
        document.documentElement.lang = this._lang;
    },

    getPhrases() {
        const keys = [
            'ph_yes','ph_no','ph_thanks','ph_please',
            'ph_help','ph_water','ph_hungry','ph_pain',
            'ph_tired','ph_love','ph_cold','ph_hot',
            'ph_bathroom','ph_ok','ph_wait','ph_come',
            'ph_sit','ph_bed','ph_medicine',
            'ph_tv','ph_light_on','ph_light_off'
        ];
        return keys.map(k => this.t(k));
    }
